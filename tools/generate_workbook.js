const problems = window.BUZZ_PROBLEMS || [];

const TOPICS = [
  { key: "limits", label: "極限", subtitle: "Limits" },
  { key: "derivatives", label: "微分與多變數", subtitle: "Derivatives" },
  { key: "integrals", label: "積分", subtitle: "Integrals" },
  { key: "series", label: "級數與收斂", subtitle: "Series" }
];

const ANSWER_KIND_LABELS = {
  numeric: "數值",
  expression: "表達式",
  antiderivative: "不定積分",
  text: "文字"
};

const orderedProblems = TOPICS.flatMap((topic) => problems.filter((problem) => problem.topic === topic.key));

function latexText(value) {
  return String(value || "")
    .replace(/\\/g, "\\textbackslash{}")
    .replace(/([{}%$&#_^])/g, "\\$1")
    .replace(/~/g, "\\textasciitilde{}");
}

function latexVerb(value) {
  const text = String(value || "");
  const delimiter = ["|", "!", "/", "+", ":", ";", "@", "#"].find((item) => !text.includes(item));
  if (!delimiter) return `\\texttt{${latexText(text)}}`;
  return `\\verb${delimiter}${text}${delimiter}`;
}

function displayAnswer(problem) {
  if (problem.answerKind === "text") return problem.canonical || (problem.answers && problem.answers[0]) || "";
  return problem.answer || "";
}

function topicInfo(topic) {
  return TOPICS.find((item) => item.key === topic) || { key: topic, label: topic, subtitle: "" };
}

function shortTags(problem) {
  const tags = (problem.tags || []).filter(Boolean).slice(0, 3);
  return tags.length ? tags.map(latexText).join(" / ") : "未標記";
}

function answerLines(problem) {
  const base = problem.answerKind === "antiderivative" || problem.topic === "integrals" ? 3 : 2;
  const bonus = (problem.rank || problem.difficulty) >= 5 ? 1 : 0;
  return Array.from({ length: base + bonus }, () => "\\answerline").join("\n");
}

function formatProblem(problem, globalIndex, topicIndex) {
  const info = topicInfo(problem.topic);
  const kind = ANSWER_KIND_LABELS[problem.answerKind] || problem.answerKind;
  const title = `${String(topicIndex + 1).padStart(2, "0")} / ${latexText(problem.id)}`;
  const meta = `${latexText(info.label)} / Rank ${problem.rank || problem.difficulty}/6 ${latexText(problem.rankLabel || "")} / ${latexText(kind)} / ${shortTags(problem)}`;
  return [
    "\\begin{problemcard}",
    `\\problemmeta{${globalIndex + 1}}{${title}}{${meta}}`,
    `\\promptmath{${problem.prompt}}`,
    answerLines(problem),
    "\\end{problemcard}"
  ].join("\n");
}

function topicSection(topic) {
  const list = problems.filter((problem) => problem.topic === topic.key);
  if (!list.length) return "";
  const blocks = list.map((problem, index) => formatProblem(problem, orderedProblems.indexOf(problem), index)).join("\n\n");
  return [
    `\\section{${latexText(topic.label)} \\textnormal{\\small ${latexText(topic.subtitle)}}}`,
    `\\topicnote{共 ${list.length} 題。題目依 BuzzCalculus 題庫順序排列，適合列印後作為練習本使用。}`,
    blocks
  ].join("\n\n");
}

function answerKeySection() {
  const rows = orderedProblems
    .map((problem, index) => {
      const no = String(index + 1).padStart(3, "0");
      return `\\item[${no}. ${latexText(problem.id)}] ${latexVerb(displayAnswer(problem))}`;
    })
    .join("\n");
  return [
    "\\clearpage",
    "\\section*{答案索引}",
    "\\addcontentsline{toc}{section}{答案索引}",
    "\\begin{description}",
    rows,
    "\\end{description}"
  ].join("\n");
}

function countByTopic() {
  return TOPICS.map((topic) => {
    const count = problems.filter((problem) => problem.topic === topic.key).length;
    return `${latexText(topic.label)} & ${count} 題\\\\`;
  }).join("\n");
}

